;-----------------------------------------------
_SetBorderColor::               ; Sets border colour by A=Nr.  

	ld  hl,#2
	add hl,sp
	ld a,(hl)
            
	di
	out	(#0x99),a        ; a=[0..15]
	ld	a, #128+#7
	out	(#0x99),a        
	ei
	ret