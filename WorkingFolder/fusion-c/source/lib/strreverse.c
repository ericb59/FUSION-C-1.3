#define __FUSIONC 2020 
#include "../../header/msx_fusion.h"


char *StrReverse(char *str)
{
    char tmp;
    unsigned int i;
    unsigned int j;

    i = 0;
    j = StrLen(str) - 1;
    while (i < j)
    {
        tmp = str[i];
        str[i] = str[j];
        str[j] = tmp;
        j--;
        i++;
    }
    return (str);
}