	.area _CODE

;-----------------------------------------------
 
 BDRCLR   =   0xF3EB       ; border color
 
_SetBorderColor::               ; Sets border colour by A=Nr.  

	ld a,l         
	di
	out	(#0x99),a        ; a=[0..15]
	ld	a, #128+#7
	ei
	out	(#0x99),a     
	ld   (BDRCLR),a   
	ret